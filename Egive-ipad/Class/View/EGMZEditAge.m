#import "EGMZEditAge.h"

@implementation EGMZEditAge


-(void)awakeFromNib{
    self.ageField.userInteractionEnabled = NO;
    self.educationField.userInteractionEnabled = NO;
    self.workField.userInteractionEnabled = NO;
    
    
    _ageLabel.textColor = MemberZoneTextColor;
    _phoneLabel.textColor = MemberZoneTextColor;
    _educationLabel.textColor = MemberZoneTextColor;
    _workLabel.textColor = MemberZoneTextColor;
}


-(void)refreshText{
    _ageLabel.text = HKLocalizedString(@"年龄组别");
    _phoneLabel.text = HKLocalizedString(@"Register_org_number");
    _educationLabel.text = HKLocalizedString(@"Educationlevel");
    _workLabel.text = HKLocalizedString(@"Occupation");
    
}


